function addition(number1, number2){
	return number1 + number2;
}

var answer1 = addition(8, 2);
document.write("8 + 2 = " + answer1 + "<br>");


function subtraction(number1, number2){
	return number1 - number2;
}

var answer1 = subtraction(5, 2);
document.write("5 - 2 = " + answer1 + "<br>");

function multiplication(number1, number2){
	return number1 * number2;
}

var answer1 = multiplication(9, 4);
document.write("9 x 4 = " + answer1 + "<br>");

function division(number1, number2){
	return number1 / number2;
}

var answer1 = division(133, 92).toFixed(2);
document.write("133 : 92 = " + answer1 + "<br>");

function increment(number1, number2){
	return number1 + number2;
}

var answer1 = increment(9, 1);
document.write("9 + 1 = " + answer1 + "<br>");

function decrement(number1, number2){
	return number1 - number2;
}

var answer1 = decrement(91, 1);
document.write("91 - 1 = " + answer1 + "<br>");
